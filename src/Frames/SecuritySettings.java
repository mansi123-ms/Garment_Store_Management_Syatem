    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int op = JOptionPane.showConfirmDialog(this, "Are you sure you want reset all your security settings");
        if (op == 0) {

            try {
                Connection con = new DatabaseConnection().getConnection();
                PreparedStatement ps = con.prepareStatement("Delete from Security");
                boolean check = ps.execute();
                if (check == false) {
                    JOptionPane.showMessageDialog(this, "Security setting changed to default successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "Something Went Wrong");
                }
                con.close();
            } catch (HeadlessException | SQLException ex) {
                ex.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String set = jTextField1.getText();
        String cnf = jTextField2.getText();
        String answer = jTextField3.getText();
        int index = jComboBox1.getSelectedIndex();
        System.out.println(index);

        if (set.equals("") || cnf.equals("") || answer.equals("") || index == 0) {
            jLabel6.setText("*Please Complete all the fields");
        } 
        else {
            jLabel6.setText("");
            
            if (!(set.equals(cnf))) {
                jLabel6.setText("*Password did not match");
            } 
            else {
                try {
                    Connection con = new DatabaseConnection().getConnection();
                    PreparedStatement ps = con.prepareStatement("Insert into Security values (?,?,?)");
                    PreparedStatement ps1 = con.prepareStatement("Select * from Security");

                    ps.setString(1, cnf);
                    ps.setString(2, jComboBox1.getSelectedItem().toString());
                    ps.setString(3, answer);

                    ResultSet rs = ps1.executeQuery();

                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "Security settings are already set ");
                    } else {
                        int check = ps.executeUpdate();
                        if (check == 1) {
                            JOptionPane.showMessageDialog(this, "Security added successfully");
                        } else {
                            JOptionPane.showMessageDialog(this, "Something Went Wrong");
                        }
                    }
                    con.close();

                } catch (HeadlessException | SQLException ex) {
                    ex.printStackTrace();
                }

            }

        }

        if (code == 200) {
            new FlashForm().setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

